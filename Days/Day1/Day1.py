print("Hello World!")
# Mit der Funktion print() können wir einen beliebigen String-Ausdruck auf der Konsole ausgeben.

print("Hello World!\nHello World!\nHello World!")
# Mit dem Befehl \n können wir innerhalb einer einzigen print()-Funktion Zeichenketten in mehreren Zeilen auf der Konsole ausgeben.

print("Mert" + " " + "Bekleviç")
# Mit der Funktion print() können wir verschiedene Zeichenketten mit + verbinden.

input("Wie heißt du?")
# Mit der Funktion input() können wir eine Eingabevariable vom Benutzer erhalten.

print("Hello" + " " + input("Wie heißt du?") + "!")
# Wir können einfach die Input-Funktion in unsere Print-Funktion integrieren.

name = input("Was ist deine Lieblingsfarbe?")
print("Deine Lieblingsfarbe ist " + name)
# Wir können diesen Prozess auch mit Variablen durchführen.

name = input("Welche Mannschaft unterstützt du?")
length = len(name)
print(length)
